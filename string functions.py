#uppercase string function.
str1 = "AbcDEfghIJ"
print(str1.upper())
#lowercase string function.
str2 = "AbcDEfghIJ"
print(str2.lower())
#strip string function.
str3 = " Silver Spoon "
print(str3.strip)
#rstrip string function.
str4 = "Hello !!!"
print(str4.rstrip("!"))
#replace string function.
str5 = "Silver Spoon"
print(str5.replace("Sp", "M"))
#split string function.
str6 = "Silver Spoon"
print(str6.split(" "))
#Splits the string at the whitespace " ".
#capitalize string function.
str7 = "hello"
capStr1 = str7.capitalize()
print(capStr1)
str8 = "hello WorlD"
capStr2 = str8.capitalize()
print(capStr2)
#center string function.
str9 = "Welcome to the Console!!!"
print(str9.center(50))
#count string function
str10 = "Abracadabra"
countStr = str10.count("a")
print(countStr)
#endwith string function.
str11 = "Welcome to the Console !!!"
print(str11.endswith("!!!"))
#index string function.
str12 = "He's name is Dan. Dan is an honest man."
print(str12.index("Dan"))
#islower string function.
str13 = "hello world"
print(str13.islower())
#isPrintable string function.
